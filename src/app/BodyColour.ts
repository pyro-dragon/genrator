export class BodyColour {
	body;
	markings;
	hair;
	nose;
	eyes;
	claws;

	constructor (body: String, markings: String, hair: String, nose: String, eyes: String, claws: String) {

		this.body = body;
		this.markings = markings;
		this.hair = hair;
		this.nose = nose;
		this.eyes = eyes;
		this.claws = claws;
	}
}
